s = 'abcd efgh ijklm'
# 1) 공백을 기준으로 자르시요(리스트의 문자열로) .
s1 = s.split()
print(s1)

# 2) ‘efgh’문자열을 추출하시요.

s2 = s1[1]
print(s2)
# 3) ‘ac fhikm’ 문자열을 추출하시요 정답.

s3 = s[::2]
print(s3)

# 4) ‘mlkji’ 문자열을 추출하시요.

s4 = s[:8:-1]
print(s4)
